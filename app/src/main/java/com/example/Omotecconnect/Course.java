package com.example.Omotecconnect;

public class Course
{
    public String course_id;
    public String course_name;
    public String sess_dura;
    public String no_sess;

    public String getId() {

        return course_id;
    }

    public void setId(String imageServerUrl) {

        this.course_id = imageServerUrl;
    }

    public String getSessions() {

        return no_sess;
    }

    public void setSessions(String imageServerUrl) {

        this.no_sess = imageServerUrl;
    }

    public String getDuration() {

        return sess_dura;
    }

    public void setDuration(String imageServerUrl) {

        this.sess_dura = imageServerUrl;
    }

    public String getCourseName() {

        return course_name;
    }

    public void setCourseName(String Imagetitlename) {

        this.course_name = Imagetitlename;
    }

}
